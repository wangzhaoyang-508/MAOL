"""
severity/scripts/eval_predicted_instances.py

Evaluate severity models on PREDICTED instances (predicted-instance setting).

IoU matching:
  - DEFAULT: polygon mask IoU using TRUE image dimensions read from the .bmp file
  - FALLBACK: bbox IoU when a polygon has < 3 vertices
  - Image W/H is always read from Track2/NG_1154/images/<stem>.bmp — never assumed

Eval modes:
  --eval_mode smoke_test   Use any available predictions (e.g. result/labels/ which
                           may be test-set predictions). Results are LABELLED AS
                           NON-FORMAL and should NOT be reported in the paper.
  --eval_mode formal       Use val-split-specific predictions generated by running
                           YOLO on the val split images. Results are formal and
                           can be reported.

Model config auto-inference:
  - Reads <checkpoint_dir>/config.json for head_type, use_morphology, etc.
  - Reads <checkpoint_dir>/morph_stats.json for E4/E5 normalisation stats
"""

import argparse
import json
import os
import sys
import datetime
from collections import defaultdict

import numpy as np
import pandas as pd
import torch
from PIL import Image as PILImage
from torch.utils.data import DataLoader
from sklearn.metrics import f1_score

sys.path.insert(0, os.path.join(os.path.dirname(__file__), "..", ".."))

from severity.datasets.severity_roi_dataset import (
    collect_instances, GRADE_NAMES, MORPH_DIM,
)
from severity.datasets.predicted_instance_dataset import (
    PredictedInstanceDataset, load_yolo_seg_predictions,
)
from severity.models.severity_classifier import build_model
from severity.utils.instance_matching import greedy_match, compute_match_stats
from severity.utils.metrics import compute_metrics, print_metrics
from severity.utils.losses import coral_predict
from severity.utils.grade_schema import GRADE_MAP

DEFAULT_IMG_DIR   = "Track2/NG_1154/images"
DEFAULT_LABEL_DIR = "Track2/NG_1154/level_labels"


# ---------------------------------------------------------------------------
# checkpoint config helpers
# ---------------------------------------------------------------------------

def load_checkpoint_config(ckpt_path: str) -> dict:
    cfg_path = os.path.join(os.path.dirname(ckpt_path), "config.json")
    if os.path.exists(cfg_path):
        with open(cfg_path) as f:
            return json.load(f)
    print(f"  [warn] config.json not found at {cfg_path}, using defaults")
    return {}


def infer_model_mode(cfg: dict) -> dict:
    """Extract model flags from saved config.json."""
    def _bool(v, default="false"):
        return str(cfg.get(v, default)).lower() in ("true", "1", "yes")
    return {
        "arch":                cfg.get("arch", "resnet18"),
        "head_type":           cfg.get("head_type", "ce"),
        "use_morphology":      _bool("use_morphology"),
        "use_class_embedding": _bool("use_class_embedding"),
        "morph_hidden_dim":    int(cfg.get("morph_hidden_dim", 32)),
        "class_emb_dim":       int(cfg.get("class_emb_dim", 16)),
        "num_defect_classes":  int(cfg.get("num_defect_classes", 11)),
        "roi_size":            int(cfg.get("roi_size", 64)),
        "pad_ratio":           float(cfg.get("pad_ratio", 0.2)),
    }


def load_morph_stats(ckpt_path: str):
    stats_path = os.path.join(os.path.dirname(ckpt_path), "morph_stats.json")
    if os.path.exists(stats_path):
        with open(stats_path) as f:
            return json.load(f)
    return None


# ---------------------------------------------------------------------------
# forward / inference
# ---------------------------------------------------------------------------

def _forward_batch(model, batch, device, use_morphology, use_class_embedding, head_type):
    if use_morphology and use_class_embedding:
        imgs, morph, cls_id, labels = batch
        logits = model(imgs.to(device), morph=morph.to(device), class_id=cls_id.to(device))
    elif use_morphology:
        imgs, morph, labels = batch
        logits = model(imgs.to(device), morph=morph.to(device))
    else:
        imgs, labels = batch
        logits = model(imgs.to(device))
    return logits, labels.to(device)


@torch.no_grad()
def run_inference(model, loader, device, head_type, use_morphology, use_class_embedding):
    model.eval()
    all_preds, all_labels = [], []
    for batch in loader:
        logits, labels = _forward_batch(
            model, batch, device, use_morphology, use_class_embedding, head_type)
        preds = coral_predict(logits) if head_type == "coral" else logits.argmax(1)
        all_preds.extend(preds.cpu().tolist())
        all_labels.extend(labels.cpu().tolist())
    return all_preds, all_labels


# ---------------------------------------------------------------------------
# single checkpoint evaluation
# ---------------------------------------------------------------------------

def evaluate_one_checkpoint(
    ckpt_path: str,
    gt_by_stem: dict,
    pred_by_stem: dict,
    val_stems: list,
    img_size_by_stem: dict,   # stem -> (W, H) read from actual image files
    output_dir: str,
    iou_thresh: float,
    class_aware: bool,
    oracle_qwk,
    workers: int,
    eval_mode: str,           # 'smoke_test' or 'formal'
) -> dict:
    os.makedirs(output_dir, exist_ok=True)

    # ── auto-infer model config ───────────────────────────────────────────
    cfg  = load_checkpoint_config(ckpt_path)
    mode = infer_model_mode(cfg)
    morph_stats = None
    if mode["use_morphology"]:
        morph_stats = load_morph_stats(ckpt_path)
        if morph_stats is None:
            print("  [warn] morph_stats.json not found — "
                  "morphology will use zero mean / unit std (results may be degraded)")
            morph_stats = {"mean": [0.0] * MORPH_DIM, "std": [1.0] * MORPH_DIM}

    print(f"\n{'='*60}")
    print(f"  checkpoint     : {ckpt_path}")
    print(f"  head_type      : {mode['head_type']}")
    print(f"  use_morphology : {mode['use_morphology']}")
    print(f"  use_class_emb  : {mode['use_class_embedding']}")
    print(f"  iou_thresh     : {iou_thresh}  class_aware: {class_aware}")
    if eval_mode == "smoke_test":
        print("  [SMOKE TEST] predictions may not be val-split-specific — "
              "results are NON-FORMAL and must NOT be reported in the paper")

    # ── matching ──────────────────────────────────────────────────────────
    all_matched_pairs  = []
    per_image_stats    = []
    per_class_gt       = defaultdict(int)
    per_class_pred     = defaultdict(int)
    per_class_matched  = defaultdict(int)
    global_iou_modes   = defaultdict(int)   # polygon / bbox counts

    for stem in val_stems:
        gt_insts   = gt_by_stem.get(stem, [])
        pred_insts = pred_by_stem.get(stem, [])

        for g in gt_insts:
            per_class_gt[g["cls_id"]] += 1
        for p in pred_insts:
            per_class_pred[p["cls_id"]] += 1

        # use TRUE image dimensions read from the actual .bmp file
        img_W, img_H = img_size_by_stem.get(stem, (1024, 1024))

        pairs, _, _, mode_counts = greedy_match(
            gt_insts, pred_insts, iou_thresh, class_aware, img_W, img_H)

        for k, v in mode_counts.items():
            global_iou_modes[k] += v

        for pair in pairs:
            gi, pi = pair["gt_idx"], pair["pred_idx"]
            g = gt_insts[gi]
            p = pred_insts[pi]
            per_class_matched[g["cls_id"]] += 1
            all_matched_pairs.append({
                # traceability
                "img_stem":    stem,
                "gt_idx":      gi,
                "pred_idx":    pi,
                "gt_cls":      g["cls_id"],
                "pred_cls":    p["cls_id"],
                "gt_severity": GRADE_NAMES[g["grade_id"]],
                "gt_grade_id": g["grade_id"],
                "iou":         pair["iou"],
                "iou_mode":    pair["iou_mode"],   # 'polygon' or 'bbox'
                # model inputs — ALL from prediction
                "img_path":    g["img_path"],       # image file path (shared)
                "pred_points": p["points_norm"],    # predicted polygon
                "pred_cls_id": p["cls_id"],         # predicted class
                "roi_source":  "pred_polygon",
                "morph_source":"pred_polygon",
                "class_source":"pred",
            })

        stats = compute_match_stats(gt_insts, pred_insts, pairs)
        stats["img_stem"] = stem
        per_image_stats.append(stats)

    num_gt      = sum(s["num_gt"]      for s in per_image_stats)
    num_pred    = sum(s["num_pred"]    for s in per_image_stats)
    num_matched = len(all_matched_pairs)

    iou_mode_summary = dict(global_iou_modes)
    dominant_mode = max(iou_mode_summary, key=iou_mode_summary.get) \
                    if iou_mode_summary else "polygon"

    print(f"  GT instances   : {num_gt}")
    print(f"  pred instances : {num_pred}")
    print(f"  matched pairs  : {num_matched}")
    print(f"  IoU mode used  : {iou_mode_summary}  (dominant: {dominant_mode})")

    if num_matched == 0:
        print("  [warn] no matched pairs — skipping severity inference")
        return {"exp": os.path.basename(os.path.dirname(ckpt_path)),
                "num_matched": 0, "qwk": None, "acc": None, "macro_f1": None,
                "matched_rate": 0.0, "iou_mode": dominant_mode}

    # ── build dataset & dataloader ────────────────────────────────────────
    ds = PredictedInstanceDataset(
        samples=all_matched_pairs,
        roi_size=mode["roi_size"],
        pad_ratio=mode["pad_ratio"],
        morph_stats=morph_stats if mode["use_morphology"] else None,
        use_class_id=mode["use_class_embedding"],
    )
    loader = DataLoader(ds, batch_size=64, shuffle=False,
                        num_workers=workers, pin_memory=True)

    # ── load model ────────────────────────────────────────────────────────
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    model = build_model(
        arch=mode["arch"],
        num_classes=4,
        pretrained=False,
        dropout=0.3,
        head_type=mode["head_type"],
        use_morphology=mode["use_morphology"],
        morph_dim=MORPH_DIM,
        morph_hidden_dim=mode["morph_hidden_dim"],
        use_class_embedding=mode["use_class_embedding"],
        num_defect_classes=mode["num_defect_classes"],
        class_emb_dim=mode["class_emb_dim"],
    ).to(device)

    ckpt = torch.load(ckpt_path, map_location=device)
    state = ckpt["model"] if "model" in ckpt else ckpt

    # ── backward compat: remap old key names ─────────────────────────────
    # Old SeverityTransferModel stored the CE head inside net.fc as a Sequential.
    # New model stores it as self.head (Linear).
    remapped = {}
    for k, v in state.items():
        if k == "net.fc.1.weight":
            remapped["head.weight"] = v
        elif k == "net.fc.1.bias":
            remapped["head.bias"] = v
        else:
            remapped[k] = v
    model.load_state_dict(remapped, strict=True)
    print(f"  loaded weights  epoch={ckpt.get('epoch','?')}  "
          f"best_kappa={ckpt.get('best_kappa','?')}")

    # ── inference ─────────────────────────────────────────────────────────
    print(f"  running severity inference on {num_matched} matched instances...")
    all_preds, all_labels = run_inference(
        model, loader, device,
        mode["head_type"], mode["use_morphology"], mode["use_class_embedding"])

    # ── metrics ───────────────────────────────────────────────────────────
    metrics  = compute_metrics(all_labels, all_preds)
    macro_f1 = float(f1_score(all_labels, all_preds, average="macro", zero_division=0))
    metrics["macro_f1"] = macro_f1

    print(f"\n  --- severity metrics (matched instances) ---")
    print_metrics(metrics, prefix="Predicted")
    print(f"  Macro-F1 : {macro_f1:.4f}")

    # ── save files ────────────────────────────────────────────────────────
    SMOKE_WARNING = (
        "NON-FORMAL: predictions are not val-split-specific (smoke_test mode). "
        "Do NOT report these numbers in the paper."
        if eval_mode == "smoke_test" else None
    )

    # 1. config.json
    config_out = {
        "eval_mode":           eval_mode,
        "smoke_test_warning":  SMOKE_WARNING,
        "checkpoint_path":     ckpt_path,
        "prediction_dir":      "result/labels",
        "iou_thresh":          iou_thresh,
        "class_aware_match":   class_aware,
        "iou_mode_dominant":   dominant_mode,
        "iou_mode_counts":     iou_mode_summary,
        "img_size_source":     "read_from_bmp",
        "roi_source":          "pred_polygon",
        "morph_source":        "pred_polygon",
        "class_source":        "pred",
        "head_type":           mode["head_type"],
        "use_morphology":      mode["use_morphology"],
        "use_class_embedding": mode["use_class_embedding"],
        "timestamp":           datetime.datetime.now().isoformat(),
    }
    with open(os.path.join(output_dir, "config.json"), "w") as f:
        json.dump(config_out, f, indent=2)

    # 2. match_summary.json
    per_class_recall = {
        str(c): round(per_class_matched[c] / per_class_gt[c], 4)
        if per_class_gt[c] > 0 else 0.0
        for c in sorted(per_class_gt)
    }
    match_summary = {
        "eval_mode":               eval_mode,
        "smoke_test_warning":      SMOKE_WARNING,
        "num_images":              len(val_stems),
        "num_gt_instances":        num_gt,
        "num_pred_instances":      num_pred,
        "num_matched":             num_matched,
        "matched_rate":            round(num_matched / num_gt,   4) if num_gt   > 0 else 0.0,
        "pred_precision_like":     round(num_matched / num_pred, 4) if num_pred > 0 else 0.0,
        "iou_mode_counts":         iou_mode_summary,
        "iou_mode_dominant":       dominant_mode,
        "per_class_gt_count":      {str(k): v for k, v in per_class_gt.items()},
        "per_class_pred_count":    {str(k): v for k, v in per_class_pred.items()},
        "per_class_matched_count": {str(k): v for k, v in per_class_matched.items()},
        "per_class_match_recall":  per_class_recall,
    }
    with open(os.path.join(output_dir, "match_summary.json"), "w") as f:
        json.dump(match_summary, f, indent=2)

    # 3. severity_metrics_matched.json
    sev_metrics = {
        "eval_mode":        eval_mode,
        "smoke_test_warning": SMOKE_WARNING,
        "accuracy":         round(metrics["overall_acc"], 4),
        "macro_f1":         round(macro_f1, 4),
        "qwk":              round(metrics["weighted_kappa"], 4),
        "num_matched_eval": num_matched,
        "confusion_matrix": metrics["confusion_matrix"].tolist(),
    }
    with open(os.path.join(output_dir, "severity_metrics_matched.json"), "w") as f:
        json.dump(sev_metrics, f, indent=2)

    # 4. matched_predictions.csv
    rows = []
    for pair, pred, gt in zip(all_matched_pairs, all_preds, all_labels):
        rows.append({
            "img_stem":     pair["img_stem"],
            "gt_idx":       pair["gt_idx"],
            "pred_idx":     pair["pred_idx"],
            "gt_class":     pair["gt_cls"],
            "pred_class":   pair["pred_cls"],
            "gt_severity":  pair["gt_severity"],
            "pred_severity":GRADE_NAMES[pred],
            "iou":          pair["iou"],
            "iou_mode":     pair["iou_mode"],
            "roi_source":   pair["roi_source"],
            "morph_source": pair["morph_source"],
            "class_source": pair["class_source"],
            "correct":      int(pred == gt),
        })
    pd.DataFrame(rows).to_csv(
        os.path.join(output_dir, "matched_predictions.csv"), index=False)

    # 5. confusion_matrix_matched.csv
    pd.DataFrame(
        metrics["confusion_matrix"], index=GRADE_NAMES, columns=GRADE_NAMES
    ).to_csv(os.path.join(output_dir, "confusion_matrix_matched.csv"))

    # 6. per_image_match_stats.csv
    pd.DataFrame(per_image_stats).to_csv(
        os.path.join(output_dir, "per_image_match_stats.csv"), index=False)

    # 7. oracle_vs_pred_gap_summary.json
    pred_qwk = round(metrics["weighted_kappa"], 4)
    gap_summary = {
        "eval_mode":         eval_mode,
        "smoke_test_warning":SMOKE_WARNING,
        "oracle_qwk":    oracle_qwk,
        "predicted_qwk": pred_qwk,
        "gap":           round(oracle_qwk - pred_qwk, 4) if oracle_qwk is not None else None,
        "num_matched":   num_matched,
        "num_gt":        num_gt,
        "matched_rate":  match_summary["matched_rate"],
        "iou_mode_dominant": dominant_mode,
    }
    with open(os.path.join(output_dir, "oracle_vs_pred_gap_summary.json"), "w") as f:
        json.dump(gap_summary, f, indent=2)

    print(f"\n  results saved to: {output_dir}")

    return {
        "exp":                 os.path.basename(os.path.dirname(ckpt_path)),
        "checkpoint":          ckpt_path,
        "use_morphology":      mode["use_morphology"],
        "use_class_embedding": mode["use_class_embedding"],
        "num_matched":         num_matched,
        "matched_rate":        match_summary["matched_rate"],
        "acc":                 round(metrics["overall_acc"], 4),
        "macro_f1":            round(macro_f1, 4),
        "qwk":                 pred_qwk,
        "iou_mode":            dominant_mode,
    }


# ---------------------------------------------------------------------------
# main
# ---------------------------------------------------------------------------

def parse_args():
    p = argparse.ArgumentParser()
    p.add_argument("--split_file",        default="severity/splits/split.json")
    p.add_argument("--img_dir",           default=DEFAULT_IMG_DIR)
    p.add_argument("--label_dir",         default=DEFAULT_LABEL_DIR)
    p.add_argument("--prediction_dir",    default="result/labels")
    p.add_argument("--checkpoint_path",   default=None)
    p.add_argument("--batch_checkpoints", nargs="+", default=None)
    p.add_argument("--output_dir",        default="severity/results_predicted/run")
    p.add_argument("--iou_thresh",        type=float, default=0.5)
    p.add_argument("--class_aware_match", default="true")
    p.add_argument("--oracle_qwk",        type=float, default=None)
    p.add_argument("--workers",           type=int,   default=4)
    p.add_argument("--eval_mode",         default="smoke_test",
                   choices=["smoke_test", "formal"],
                   help="smoke_test: non-val-specific predictions (pipeline check only); "
                        "formal: val-split-specific predictions (reportable results)")
    return p.parse_args()


def main():
    args = parse_args()
    class_aware = args.class_aware_match.lower() in ("true", "1", "yes")

    if args.eval_mode == "smoke_test":
        print("\n" + "!"*60)
        print("  SMOKE TEST MODE — predictions may not be val-split-specific.")
        print("  Use this only to verify the pipeline and output file structure.")
        print("  Do NOT report these numbers in the paper.")
        print("  For formal evaluation, re-run YOLO on val split images and")
        print("  use --eval_mode formal.")
        print("!"*60 + "\n")
    else:
        print("\n  [formal eval] predictions are expected to be val-split-specific.\n")

    # ── load split ────────────────────────────────────────────────────────
    with open(args.split_file) as f:
        sp = json.load(f)
    val_stems = sp["val_stems"]
    print(f"val split: {len(val_stems)} images  ({args.split_file})")

    # ── read TRUE image dimensions from BMP files ─────────────────────────
    # This is mandatory — never assume a default size for polygon rasterisation.
    print("reading image dimensions from BMP files...")
    img_size_by_stem = {}
    n_missing_imgs = 0
    for stem in val_stems:
        img_path = os.path.join(args.img_dir, stem + ".bmp")
        if os.path.exists(img_path):
            with PILImage.open(img_path) as im:
                img_size_by_stem[stem] = im.size   # (W, H)
        else:
            n_missing_imgs += 1
            img_size_by_stem[stem] = (1024, 1024)  # last-resort fallback, logged
    if n_missing_imgs:
        print(f"  [warn] {n_missing_imgs} image files not found; "
              f"used (1024,1024) fallback for polygon IoU rasterisation")
    print(f"  image sizes loaded for {len(img_size_by_stem) - n_missing_imgs} images")

    # ── load GT annotations ───────────────────────────────────────────────
    print("loading GT annotations...")
    all_inst = collect_instances(args.img_dir, args.label_dir, compute_morph=False)
    val_stem_set = set(val_stems)
    gt_by_stem = defaultdict(list)
    for inst in all_inst:
        if inst["img_stem"] in val_stem_set:
            gt_by_stem[inst["img_stem"]].append({
                "cls_id":      inst["cls_id"],
                "grade_id":    inst["grade_id"],
                "points_norm": inst["points_norm"],
                "img_path":    inst["img_path"],
            })
    print(f"  GT instances in val: {sum(len(v) for v in gt_by_stem.values())}")

    # ── load predictions ──────────────────────────────────────────────────
    print("loading predictions...")
    pred_by_stem = load_yolo_seg_predictions(args.prediction_dir, val_stems)
    print(f"  pred instances in val: {sum(len(v) for v in pred_by_stem.values())}")

    # ── determine checkpoints ─────────────────────────────────────────────
    if args.batch_checkpoints:
        checkpoints = args.batch_checkpoints
    elif args.checkpoint_path:
        checkpoints = [args.checkpoint_path]
    else:
        print("ERROR: provide --checkpoint_path or --batch_checkpoints")
        sys.exit(1)

    # ── evaluate ──────────────────────────────────────────────────────────
    summary_rows = []
    for ckpt_path in checkpoints:
        if not os.path.exists(ckpt_path):
            print(f"[warn] checkpoint not found: {ckpt_path}, skipping")
            continue
        exp_name = os.path.basename(os.path.dirname(ckpt_path))
        out_dir = os.path.join(args.output_dir, exp_name) \
                  if len(checkpoints) > 1 else args.output_dir

        row = evaluate_one_checkpoint(
            ckpt_path=ckpt_path,
            gt_by_stem=gt_by_stem,
            pred_by_stem=pred_by_stem,
            val_stems=val_stems,
            img_size_by_stem=img_size_by_stem,
            output_dir=out_dir,
            iou_thresh=args.iou_thresh,
            class_aware=class_aware,
            oracle_qwk=args.oracle_qwk,
            workers=args.workers,
            eval_mode=args.eval_mode,
        )
        summary_rows.append(row)

    # ── batch summary ─────────────────────────────────────────────────────
    if len(summary_rows) > 1:
        summary_path = os.path.join(args.output_dir, "predicted_setting_summary.csv")
        df = pd.DataFrame(summary_rows)
        df.to_csv(summary_path, index=False)
        print(f"\n{'='*60}")
        print(f"  batch summary: {summary_path}")
        print(df.to_string(index=False))
        if args.eval_mode == "smoke_test":
            print("\n  [SMOKE TEST] above numbers are NON-FORMAL.")


if __name__ == "__main__":
    main()
